package model;

public class UsePoint {
	private int gameId ;
	private int levelId;
	private int usePount;
	
	
	
	public int getGameId() {
		return gameId;
	}
	public void setGameId(int gameId) {
		this.gameId = gameId;
	}
	public int getLevelId() {
		return levelId;
	}
	public void setLevelId(int levelId) {
		this.levelId = levelId;
	}
	public int getUsePount() {
		return usePount;
	}
	public void setUsePount(int usePount) {
		this.usePount = usePount;
	}
	
	
	
}
